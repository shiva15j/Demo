package loopdemo;

import java.util.Scanner;

public class Demo1 {

	// ctrl+a

	// ctrl+shift+f:- for formatting the code

	public static void main(String[] args) {

		int num = 5;

		int fact = 1;

		for (int i = 1; i <= num; i++) {

			fact = fact * i;

		}
		System.out.println("Fact=:" + fact);

	}

}
